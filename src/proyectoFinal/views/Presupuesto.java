 private boolean comprobar (String nom){
    boolean ok = false;
    for (Ciudad c : cd.listarCiudad()){
    for (Ciudad c2: cd.ciudadesInactivas()){    
    if (c.getNombre() == nom || c2.getNombre() == nom){
    ok = true;
    }
    }
    ok = true;
    }
    return ok;
    }
